const assert = require('assert');
const fs = require('fs');
const os = require('os');
const path = require('path');
const { spawnSync } = require('child_process');
const { transpileMulda, compileMulda } = require('../compiler/src/transpile');
const { parseCommandArgs, resolveCToolchain, writeNativeArtifactMetadata, getCCompileArgs, resolveBuildTimestamp } = require('../runtime/src/mulda');
const { resolveSourceFileInput } = require('../scripts/build-cross-c');

function testVariablesAndPrint() {
  const source = ['Hokna', 'dej x = 40 + 2', 'vyblij "Ahoj"', 'vyblij x'].join('\n');
  const c = transpileMulda(source);
  assert(c.includes('__mulda_trace("PRINT_EXPR"'));
  assert(c.includes('double x = (40 + 2);'));
  assert(c.includes('printf("%s\\n", "Ahoj");'));
}

function testAssignmentStatementParsingAndTranspile() {
  const source = ['Hokna', 'dej x = 1', 'x = x + 2', 'vyblij x'].join('\n');
  const { ast, c } = compileMulda(source);
  assert.deepStrictEqual(ast[1], { type: 'assign', name: 'x', expr: 'x + 2', line: 3 });
  assert(c.includes('x = (x + 2);'));
  assert(c.includes('__mulda_trace_num_var("ASSIGN"'));
}

function testControlFlowBlocks() {
  const source = ['Hokna', 'dej x = 2', 'opakuj 3', '  dyz x > 0', '    vyblij x', '  konec', 'konec'].join('\n');
  const c = transpileMulda(source);
  assert(c.includes('for (int __mulda_i1 = 0; __mulda_i1 < (int)(3); __mulda_i1 += 1) {'));
  assert(c.includes('if ((x > 0)) {'));
}

function testBooleanTokensAndTypeAnnotation() {
  const source = ['Hokna', 'dej jeTo: joNeboHovno = jo', 'dyz jeTo aKurva nechcu hovno', '  vyblij jeTo bo hovno', 'konec'].join('\n');
  const c = transpileMulda(source);
  assert(c.includes('bool jeTo = (true);'));
  assert(c.includes('if ((jeTo && ! false)) {'));
}

function testFunctionDeclarationAndReturn() {
  const source = ['Hokna', 'funkcicka secti(a, b)', '  vrat a + b', 'konec', 'vyblij secti(1, 2)'].join('\n');
  const c = transpileMulda(source);
  assert(c.includes('static double secti(double a, double b)'));
  assert(c.includes('return (a + b);'));
}

function testDeprecatedAliasesStillWork() {
  const source = ['nacpi', 'funkce secti(a, b)', '  kdyz a > 0', '    vrat a + b', '  konec', '  vrat b', 'konec', 'rekni Nazdar', 'spocitej secti(1, 1)'].join('\n');
  const c = transpileMulda(source);
  assert(c.includes('static double secti(double a, double b)'));
  assert(c.includes('printf("Nazdar\\n");'));
}

function testStartKeywordRequiredAndUnique() {
  assert.throws(() => transpileMulda('vyblij 1'), /Program must start with "Hokna"/);
  assert.throws(() => transpileMulda(['Hokna', 'vyblij 1', 'Hokna'].join('\n')), /allowed only once/);
}

function testUnexpectedKonecError() {
  assert.throws(() => transpileMulda(['Hokna', 'konec'].join('\n')), /Unexpected "konec"/);
}

function testAssignmentValidationErrors() {
  assert.throws(() => transpileMulda(['Hokna', '123 = 1'].join('\n')), /Unsupported syntax/);
  assert.throws(() => transpileMulda(['Hokna', 'x = '].join('\n')), /Assignment requires expression after "="/);
}

function testCBackendGenerationAndArgs() {
  const source = ['Hokna', 'dej x = 1', 'x = x + 2', 'dyz x > 2', '  vyblij x', 'konec'].join('\n');
  const { c } = compileMulda(source);
  assert(c.includes('generated by mulda transpiler v0.9.0'));

  const parsedCompile = parseCommandArgs(['--platform', 'windows-x64', 'examples/hello.mulda']);
  assert.strictEqual(parsedCompile.options.platform, 'windows-x64');

  const parsedRun = parseCommandArgs(['--trace-json', 'examples/hello.mulda']);
  assert.strictEqual(parsedRun.options.trace, true);
  assert.strictEqual(parsedRun.options.traceFormat, 'json');

  assert.deepStrictEqual(resolveCToolchain('linux-x64'), { cc: 'gcc', outputExt: '', targetLabel: 'linux-x64' });
  assert.deepStrictEqual(resolveCToolchain('windows-x64'), { cc: 'x86_64-w64-mingw32-gcc', outputExt: '.exe', targetLabel: 'windows-x64' });
  assert.strictEqual(resolveCToolchain('darwin-arm64'), null);
}

function testRemovedBackendFlagsFailFast() {
  assert.throws(() => parseCommandArgs(['--target', 'js', 'examples/hello.mulda']), /no longer supported/);
  assert.throws(() => parseCommandArgs(['--bc', 'examples/hello.mulda']), /no longer supported/);
}

function testCCompileArgsIncludeDeterministicWindowsFlag() {
  const linuxArgs = getCCompileArgs(resolveCToolchain('linux-x64'), 'in.c', 'out');
  assert.deepStrictEqual(linuxArgs, ['in.c', '-std=c11', '-O2', '-o', 'out']);

  const windowsArgs = getCCompileArgs(resolveCToolchain('windows-x64'), 'in.c', 'out.exe');
  assert.deepStrictEqual(windowsArgs, ['in.c', '-std=c11', '-O2', '-Wl,--no-insert-timestamp', '-o', 'out.exe']);
}

function testBuildTimestampUsesSourceDateEpochWhenValid() {
  const original = process.env.SOURCE_DATE_EPOCH;
  try {
    process.env.SOURCE_DATE_EPOCH = '1710000000';
    assert.strictEqual(resolveBuildTimestamp(), new Date(1710000000 * 1000).toISOString());

    process.env.SOURCE_DATE_EPOCH = 'invalid';
    const fallback = resolveBuildTimestamp();
    assert.ok(/\d{4}-\d{2}-\d{2}T/.test(fallback));
  } finally {
    if (original === undefined) delete process.env.SOURCE_DATE_EPOCH;
    else process.env.SOURCE_DATE_EPOCH = original;
  }
}

function testWriteNativeArtifactMetadataContainsCBackend() {
  const tmp = fs.mkdtempSync(path.join(os.tmpdir(), 'mulda-meta-'));
  const artifact = path.join(tmp, 'hello-linux-x64');
  fs.writeFileSync(artifact, 'bin');

  writeNativeArtifactMetadata({
    sourceFile: '/tmp/hello.mulda',
    cFile: '/tmp/hello.c',
    artifactFile: artifact,
    toolchain: resolveCToolchain('linux-x64'),
    platform: 'linux-x64'
  });

  const metadataPath = `${artifact}.metadata.json`;
  const payload = JSON.parse(fs.readFileSync(metadataPath, 'utf8'));
  assert.strictEqual(payload.backend, 'c');
  assert.strictEqual(payload.platform, 'linux-x64');
}

function testResolveSourceFileInputValidatesMuldaFile() {
  const tmp = fs.mkdtempSync(path.join(os.tmpdir(), 'mulda-src-'));
  const sourceFile = path.join(tmp, 'hello.mulda');
  fs.writeFileSync(sourceFile, 'Hokna\nvyblij 1\n');

  const ok = resolveSourceFileInput(sourceFile);
  assert.strictEqual(ok.ok, true);
  assert.strictEqual(ok.sourceFile, sourceFile);

  const invalidExt = resolveSourceFileInput(path.join(tmp, 'hello.txt'));
  assert.strictEqual(invalidExt.ok, false);
  assert.match(invalidExt.message, /expected a \.mulda file/);

  const missing = resolveSourceFileInput(path.join(tmp, 'missing.mulda'));
  assert.strictEqual(missing.ok, false);
  assert.match(missing.message, /Source file not found/);
}

function testCompileCliRejectsNonCOutputTarget() {
  const script = path.join(__dirname, '..', 'compiler', 'src', 'transpile.js');
  const input = path.join(__dirname, 'fixtures', 'hello.json');
  const tmp = fs.mkdtempSync(path.join(os.tmpdir(), 'mulda-compile-cli-'));
  const sourcePath = path.join(tmp, 'hello.mulda');
  fs.writeFileSync(sourcePath, 'Hokna\nvyblij 1\n');

  const result = spawnSync(process.execPath, [script, sourcePath, path.join(tmp, 'out.js')], { encoding: 'utf8' });
  assert.notStrictEqual(result.status, 0);
  assert.match(result.stderr + result.stdout, /Only \.c output is supported/);
}

function main() {
  testVariablesAndPrint();
  testAssignmentStatementParsingAndTranspile();
  testControlFlowBlocks();
  testBooleanTokensAndTypeAnnotation();
  testFunctionDeclarationAndReturn();
  testDeprecatedAliasesStillWork();
  testStartKeywordRequiredAndUnique();
  testUnexpectedKonecError();
  testAssignmentValidationErrors();
  testCBackendGenerationAndArgs();
  testRemovedBackendFlagsFailFast();
  testCCompileArgsIncludeDeterministicWindowsFlag();
  testBuildTimestampUsesSourceDateEpochWhenValid();
  testWriteNativeArtifactMetadataContainsCBackend();
  testResolveSourceFileInputValidatesMuldaFile();
  testCompileCliRejectsNonCOutputTarget();
  console.log('tests passed');
}

main();
