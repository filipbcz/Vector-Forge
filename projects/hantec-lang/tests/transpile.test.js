function testNativeArtifactMetadataSidecar() {
  const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'mulda-meta-'));
  const artifactPath = path.join(tmpDir, 'hello-linux-x64');

  writeNativeArtifactMetadata({
    sourceFile: '/workspace/examples/hello.mulda',
    cFile: '/workspace/dist/hello.c',
    artifactFile: artifactPath,
    toolchain: { cc: 'gcc', outputExt: '', targetLabel: 'linux-x64' },
    platform: 'linux-x64'
  });

  const metadataPath = `${artifactPath}.metadata.json`;
  assert.strictEqual(fs.existsSync(metadataPath), true);

  const metadata = JSON.parse(fs.readFileSync(metadataPath, 'utf8'));
  assert.strictEqual(metadata.lang, 'mulda');
  assert.strictEqual(metadata.backend, 'c');
  assert.strictEqual(metadata.platform, 'linux-x64');
  assert.strictEqual(metadata.compiler, 'gcc');
  assert.strictEqual(metadata.artifactFile, artifactPath);
  assert.strictEqual(typeof metadata.version, 'string');
  assert.strictEqual(typeof metadata.generatedAt, 'string');
}

function testCrossBuildManifestScript() {
  const projectRoot = path.resolve(__dirname, '..');
  const script = path.join(projectRoot, 'scripts/build-cross-c.js');
  const source = path.join(projectRoot, 'examples/hello.mulda');
  const run = spawnSync(process.execPath, [script, source], {
    cwd: projectRoot,
    encoding: 'utf8'
  });

  const runFromElsewhere = spawnSync(process.execPath, [script, 'examples/hello.mulda'], {
    cwd: os.tmpdir(),
    encoding: 'utf8'
  });

  const manifestPath = path.join(projectRoot, 'dist/hello.release-manifest.json');
  assert.strictEqual(fs.existsSync(manifestPath), true);
  const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
  assert.strictEqual(manifest.lang, 'mulda');
  assert.strictEqual(manifest.version, '1.0.0-rc.2');
  assert.strictEqual(typeof manifest.cSource, 'string');
  assert.strictEqual(typeof manifest.cSourceSha256, 'string');
  assert.strictEqual(manifest.cSourceSha256.length, 64);
  assert(manifest.platforms['linux-x64']);
  assert(manifest.platforms['windows-x64']);
  assert(['ok', 'failed'].includes(manifest.platforms['linux-x64'].status));
  assert(['ok', 'failed'].includes(manifest.platforms['windows-x64'].status));

  // Script returns non-zero when some toolchain is unavailable (expected in many local envs).
  assert([0, 2].includes(run.status || 0));
  assert([0, 2].includes(runFromElsewhere.status || 0));
}

function testCTraceSnapshotsWhenGccAvailable() {
  const gcc = spawnSync('gcc', ['--version'], { stdio: 'ignore' });
  if (gcc.status !== 0) {
    console.log('skip c trace snapshot e2e (gcc unavailable)');
    return;
  }

  const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'mulda-c-trace-'));
  const cPath = path.join(tmpDir, 'demo.c');
  const binPath = path.join(tmpDir, 'demo.bin');

  const { c } = compileMulda(['Hokna', 'dej x = 2', 'x = x + 3', 'vyblij x'].join('\n'));
  fs.writeFileSync(cPath, c, 'utf8');

  const cc = spawnSync('gcc', [cPath, '-std=c11', '-O2', '-o', binPath], { stdio: 'inherit' });
  assert.strictEqual(cc.status || 0, 0, 'gcc compile should pass (trace snapshot)');

  const run = spawnSync(binPath, {
    encoding: 'utf8',
    env: {
      ...process.env,
      MULDA_TRACE: '1',
      MULDA_TRACE_FORMAT: 'json'
    }
  });
  assert.strictEqual(run.status || 0, 0, 'c binary should run (trace snapshot)');

  const events = (run.stderr || '')
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => JSON.parse(line));

  assert(events.some((event) => event.op === 'DECLARE' && event.detail === 'x=2'));
  assert(events.some((event) => event.op === 'ASSIGN' && event.detail === 'x=5'));

  const snapshots = events.filter((event) => event.op === 'SNAPSHOT' && /^x=/.test(event.detail || ''));
  assert.deepStrictEqual(snapshots.map((event) => event.detail), ['x=2', 'x=5']);
}

