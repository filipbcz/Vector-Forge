const assert = require('assert');
const { transpileMulda, compileMulda } = require('../compiler/src/transpile');
const { runBytecodeObject } = require('../runtime/src/vm');
const { parseArgs } = require('../runtime/src/run');
const { parseCommandArgs } = require('../runtime/src/mulda');

function testVariablesAndPrint() {
  const source = [
    '# test',
    'Hokna',
    'dej x = 40 + 2',
    'vyblij "Ahoj"',
    'vyblij x'
  ].join('\n');

  const js = transpileMulda(source);
  assert(js.includes('const x = (40 + 2);'));
  assert(js.includes('console.log("Ahoj");'));
  assert(js.includes('console.log(x);'));
}

function testControlFlowBlocks() {
  const source = [
    'Hokna',
    'dej x = 2',
    'opakuj 3',
    '  kdyz x > 0',
    '    vyblij x',
    '  konec',
    'konec'
  ].join('\n');

  const js = transpileMulda(source);
  assert(js.includes('for (let __mulda_i1 = 0; __mulda_i1 < (3); __mulda_i1 += 1) {'));
  assert(js.includes('if ((x > 0)) {'));
  assert(js.includes('console.log(x);'));
}

function testBooleanTokensAndTypeAnnotation() {
  const source = [
    'Hokna',
    'dej jeTo: joNeboHovno = jo',
    'kdyz jeTo aKurva nechcu hovno',
    '  vyblij jeTo bo hovno',
    'konec'
  ].join('\n');

  const js = transpileMulda(source);
  assert(js.includes('const jeTo = (true);'));
  assert(js.includes('if ((jeTo && ! false)) {'));
  assert(js.includes('console.log(jeTo || false);'));
}

function testFunctionDeclarationAndReturn() {
  const source = [
    'funkce secti(a, b)',
    '  dej sum = a + b',
    '  kdyz sum > 0',
    '    vrat sum',
    '  konec',
    '  vrat 0',
    'konec'
  ].join('\n');

  const js = transpileMulda(source);
  assert(js.includes('// generated by mulda transpiler v0.6.0'));
  assert(js.includes('function secti(a, b) {'));
}

function testDeprecatedAliasesStillWork() {
  const source = ['nacpi', 'rekni Nazdar', 'spocitej 1 + 1'].join('\n');
  const js = transpileMulda(source);
  assert(js.includes('console.log("Nazdar");'));
  assert(js.includes('console.log(1 + 1);'));
}

function testUnexpectedKonecError() {
  assert.throws(() => transpileMulda('konec'), /Unexpected "konec"/);
}

function testBytecodeVmRunner() {
  const source = [
    'Hokna',
    'funkce secti(a, b)',
    '  vrat a + b',
    'konec',
    'dej n = 2',
    'opakuj 2',
    '  kdyz n > 0',
    '    vyblij secti(n, 3)',
    '  konec',
    'konec',
    'vyblij obsahuje("mulda", "ul")'
  ].join('\n');

  const { bytecode } = compileMulda(source);
  assert.strictEqual(bytecode.version, '0.6.0');
  assert.strictEqual(bytecode.target, 'mulda-vm');

  const lines = [];
  runBytecodeObject(bytecode, { stdout: (line) => lines.push(String(line)) });
  assert.deepStrictEqual(lines, ['5', '5', 'true']);
}

function testRuntimeArgParsers() {
  const runtimeParsed = parseArgs(['--trace', 'dist/demo.bytecode.json']);
  assert.strictEqual(runtimeParsed.options.trace, true);
  assert.deepStrictEqual(runtimeParsed.positional, ['dist/demo.bytecode.json']);

  const muldaParsed = parseCommandArgs(['--debug', 'examples/hello.mulda']);
  assert.strictEqual(muldaParsed.options.trace, true);
  assert.strictEqual(muldaParsed.inputFile, 'examples/hello.mulda');
}

testVariablesAndPrint();
testControlFlowBlocks();
testBooleanTokensAndTypeAnnotation();
testFunctionDeclarationAndReturn();
testDeprecatedAliasesStillWork();
testUnexpectedKonecError();
testBytecodeVmRunner();
testRuntimeArgParsers();
console.log('tests passed');
