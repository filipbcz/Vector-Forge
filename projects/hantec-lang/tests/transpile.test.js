const assert = require('assert');
const { transpileHantec, compileHantec } = require('../compiler/src/transpile');
const { runBytecodeObject } = require('../runtime/src/vm');

function testVariablesAndPrint() {
  const source = [
    '# test',
    'dej x = 40 + 2',
    'rekni Ahoj',
    'spocitej x'
  ].join('\n');

  const js = transpileHantec(source);
  assert(js.includes('const x = (40 + 2);'));
  assert(js.includes('console.log("Ahoj");'));
  assert(js.includes('console.log(x);'));
}

function testControlFlowBlocks() {
  const source = [
    'dej x = 2',
    'opakuj 3',
    '  kdyz x > 0',
    '    spocitej x',
    '  konec',
    'konec'
  ].join('\n');

  const js = transpileHantec(source);
  assert(js.includes('for (let __hantec_i1 = 0; __hantec_i1 < (3); __hantec_i1 += 1) {'));
  assert(js.includes('if ((x > 0)) {'));
  assert(js.includes('console.log(x);'));
}

function testFunctionDeclarationAndReturn() {
  const source = [
    'funkce secti(a, b)',
    '  dej sum = a + b',
    '  kdyz sum > 0',
    '    vrat sum',
    '  konec',
    '  vrat 0',
    'konec'
  ].join('\n');

  const js = transpileHantec(source);
  assert(js.includes('// generated by hantec transpiler v0.5.1'));
  assert(js.includes('function secti(a, b) {'));
  assert(js.includes('  const sum = (a + b);'));
  assert(js.includes('  if ((sum > 0)) {'));
  assert(js.includes('    return (sum);'));
  assert(js.includes('  return (0);'));
}

function testUnexpectedKonecError() {
  const source = 'konec';
  assert.throws(
    () => transpileHantec(source),
    /Unexpected "konec"/
  );
}

function testMissingKonecError() {
  const source = [
    'kdyz 1 < 2',
    '  rekni ano'
  ].join('\n');

  assert.throws(
    () => transpileHantec(source),
    /missing closing "konec"/
  );
}

function testLineColumnError() {
  const source = 'dej 123 = 1';
  assert.throws(
    () => transpileHantec(source),
    /line 1, col 1/
  );
}

function testReturnOutsideFunctionError() {
  const source = 'vrat 1';
  assert.throws(
    () => transpileHantec(source),
    /only allowed inside funkce blocks/
  );
}

function testInvalidFunctionNameError() {
  const source = 'funkce 123(a)';
  assert.throws(
    () => transpileHantec(source),
    /Invalid function name/
  );
}

function testInvalidFunctionParameterError() {
  const source = 'funkce secti(a, 2b)';
  assert.throws(
    () => transpileHantec(source),
    /Invalid function parameter/
  );
}

function testStdlibPreludeAndUsage() {
  const source = [
    'dej txt = "abc"',
    'spocitej delka(txt)',
    'spocitej cislo("42")',
    'spocitej text(123)',
    'spocitej minimum(10, "3", 8)',
    'spocitej maximum(10, "3", 8)',
    'spocitej obsahuje("ahoj", "ho")'
  ].join('\n');

  const js = transpileHantec(source);
  assert(js.includes('function delka(value) {'));
  assert(js.includes('function cislo(value) {'));
  assert(js.includes('function text(value) {'));
  assert(js.includes('function minimum(...values) {'));
  assert(js.includes('function maximum(...values) {'));
  assert(js.includes('function obsahuje(container, needle) {'));
  assert(js.includes('console.log(delka(txt));'));
  assert(js.includes('console.log(cislo("42"));'));
  assert(js.includes('console.log(text(123));'));
  assert(js.includes('console.log(minimum(10, "3", 8));'));
  assert(js.includes('console.log(maximum(10, "3", 8));'));
  assert(js.includes('console.log(obsahuje("ahoj", "ho"));'));
}

function testStdlibArgumentValidation() {
  const js = transpileHantec('spocitej minimum()');
  assert(js.includes("throw new Error('minimum() requires at least one argument');"));
  assert(js.includes("throw new Error('maximum() requires at least one argument');"));
}

function testBytecodeVmRunner() {
  const source = [
    'funkce secti(a, b)',
    '  vrat a + b',
    'konec',
    'dej n = 2',
    'opakuj 2',
    '  kdyz n > 0',
    '    spocitej secti(n, 3)',
    '  konec',
    'konec',
    'spocitej obsahuje("mulda", "ul")'
  ].join('\n');

  const { bytecode } = compileHantec(source);
  assert.strictEqual(bytecode.version, '0.5.1');
  assert(Array.isArray(bytecode.instructions));

  const lines = [];
  runBytecodeObject(bytecode, { stdout: (line) => lines.push(String(line)) });
  assert.deepStrictEqual(lines, ['5', '5', 'true']);
}

testVariablesAndPrint();
testControlFlowBlocks();
testFunctionDeclarationAndReturn();
testUnexpectedKonecError();
testMissingKonecError();
testLineColumnError();
testReturnOutsideFunctionError();
testInvalidFunctionNameError();
testInvalidFunctionParameterError();
testStdlibPreludeAndUsage();
testStdlibArgumentValidation();
testBytecodeVmRunner();
console.log('tests passed');
